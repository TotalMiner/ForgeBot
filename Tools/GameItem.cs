        public static ItemDataXML GetItem(string itemid)
        {
            ItemDataXML _Item;
            try
            {
                _Item = itemDatas[(int)(Item)Enum.Parse(typeof(Item), itemid, true)];
            }
            catch
            {
                _Item = null;

            }
            return _Item;

        }

        #endregion GameItem

        #region GameItem.Blueprint

